// Create QuotientReminder Class to compute the Quotient and Reminder
class QuotientReminder {
    public static void main(String[] args) {
       // Create variables for number1 and number2
       int number1 = 15,number2=4;
       // Calculate the quotient using division operator
       int quotient = number1/number2;
       // Calculate the reminder using modulus operator
       int reminder = number1%number2;
       // display the quotient and reminder
       System.out.println("The Quotient is " + quotient + " and Reminder is " + reminder + " of two number " + number1 + " and " + number2);
    }
 }
 